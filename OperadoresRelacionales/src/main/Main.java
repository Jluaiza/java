package main;

public class Main {

	public static void main(String[] args) {
		// Operadores relacionales
		
		int variable1 = 400, variable2 = 200 ;
		boolean resultado;
		
		resultado = variable1 > variable2;
		System.out.println(resultado);
		
		resultado = variable1 < variable2;
		System.out.println(resultado);
		
		resultado = variable1 >= variable2;
		System.out.println(resultado);
		
		resultado = variable1 <= variable2;
		System.out.println(resultado);
		
		resultado = variable1 == variable2;
		System.out.println(resultado);
		
		resultado = variable1 != variable2; // != Diferente
		System.out.println(resultado);
	}

}
